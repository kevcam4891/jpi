                        <div class="clipTitle">Our History</div>
                        <p>Some of the greatest  inventions and the best ideas were conceived while sharing a meal. In  1999, after a church worship service and the usual Sunday family  gathering, a “brainstorming” session erupted and the concept for JPI  was born. JPI, an acronym for Just People, Inc. has a sentimental  undertone, yet speaks volumes to those with disabilities who just want  to be “normal” like everyone else. </p>
                        <p>The future owner and  administrator would see this idea come to fruition through their  seemingly endless hours of blood, sweat, and tears. The market was,  and still is “ripe” for those with disabilities to be taught a measure  of independence, self-confidence, and to be treated with dignity and  respect. </p>
                        <p>JPI has become one of  the fastest growing, exemplary Residential Services in the Hampton  Roads area (Chesapeake), with an emphasis on improving the lives of  those they serve. The true measure of success is how the client has  progressed. It’s been 10 years, yet it seems like it was only yesterday  that we sat down at that Sunday dinner… we are forever grateful for  impacting the lives of others.</p>
